# 5. Crie um programa que solicite três valores numéricos à pessoa usuária e imprima a soma dos três valores. 

valor_01 = int(input('Digite um valor inteiro: '))
valor_02 = int(input('Digite outro valor inteiro: '))
valor_03 = int(input('Digite mais um valor inteiro: '))

