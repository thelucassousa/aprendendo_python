# 6. Crie um programa que solicite dois valores numéricos à pessoa usuária e imprima a subtração do primeiro pelo o segundo valor.

idade = int(input('Qual a sua idade? '))
ano = int(input('Em que ano você está? '))
print(f'Então você tem atualmanete {idade} anos e contando que o ano atual é {ano}, você nasceu por volta de {ano - idade}, não sendo este ano um valor exato devido aos meses.')